package my;

public class SetterGetter {

	private int number;
	private String name;
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	//	getter 方法
	public int getNumber() {
		return this.number;
	}
	
//	setter方法
	public void setNumber(int number) {
		this.number = number;
	}
}
